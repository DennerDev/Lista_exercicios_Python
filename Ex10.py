x = int(input("Digite o numero que represente o mês atual "))

if x == 1:

    print(x, ' = Janeiro')

elif x == 2:

    print(x, ' = Fevereiro')

elif x == 3:

    print(x, ' = Março')

elif x == 4:

    print(x, ' = Abril')

elif x == 5:

    print(x, ' = Maio')

elif x == 6:

    print(x, ' = Junho')

elif x == 7:

    print(x, ' = Julho')

elif x == 8:

    print(x, ' = Agosto')

elif x == 9:

    print(x, ' = Setembro')

elif x == 10:

    print(x, ' = Outubro')

elif x == 11:

    print(x, ' = Novembro')

elif x == 12:

    print(x, ' = Dezembro')

else:  # Caso X não seja nenhum dos valores acima

    print('Não existe um mês com esse número...')
